-----------------------------------
-- Sleep Blast Alt
-- Family: Wyrms
-- Description: Deals Ice damage to a single target.
-- Notes: Used Jormungand while flying in place of standard attacks.
-----------------------------------
---@type TMobSkill
local mobskillObject = {}

mobskillObject.onMobSkillCheck = function(target, mob, skill)
    return 0
end

mobskillObject.onMobWeaponSkill = function(target, mob, skill)
    local params = {}

    params.baseDamage = mob:getMainLvl() + 2
    params.fTP        = { 4, 4, 4 }
    params.element    = xi.element.ICE

    local info   = xi.mobskills.mobMagicalMove(mob, target, skill, params)
    local damage = xi.mobskills.mobFinalAdjustments(info.damage, mob, skill, target, xi.attackType.MAGICAL, xi.damageType.ICE, xi.mobskills.shadowBehavior.IGNORE_SHADOWS, info.hitsLanded)

    if not xi.mobskills.hasMissMessage(mob, target, skill, damage) then
        target:takeDamage(damage, mob, xi.attackType.MAGICAL, xi.damageType.ICE)

        skill:setMsg(xi.msg.basic.HIT_DMG) -- TODO: Handle in mobFinalAdjustments once we pass params
    end

    return damage
end

return mobskillObject
